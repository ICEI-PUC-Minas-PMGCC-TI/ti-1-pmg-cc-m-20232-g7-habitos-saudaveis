function pesquisarAlimentos() {
  const consulta = document.getElementById('alimento').value.toLowerCase();
  const resultadoUl = document.getElementById('resultado');
  resultadoUl.innerHTML = '';

  if (!consulta) {
    const errorElement = document.querySelector('.error');
    errorElement.classList.remove('error-hidden');
    return;
  }

  const alimentosFiltrados = al.alimentos.filter(alimento => alimento.nome.toLowerCase().includes(consulta));

  if (alimentosFiltrados.length === 0) {
    resultadoUl.innerHTML = 'Nenhum resultado encontrado.';
  } else {
    alimentosFiltrados.forEach(alimento => {
      const li = document.createElement('li');
      li.textContent = `Nome: ${alimento.nome}, Categoria: ${alimento.categoria}, Calorias: ${alimento.CaloriaPor100g}`;
      resultadoUl.appendChild(li);
    });
  }
}



